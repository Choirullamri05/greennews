                    <h2>Selamat Hari Gunung Internasional</h2>
                    Sejak ditetapkan oleh Perserikatan Bangsa-Bangsa (PBB) pada tahun 2003, Hari Gunung Internasional yang diperingati setiap 11 Desember menjadi ajakan bagi dunia untuk menyadari betapa vitalnya peran pegunungan dalam menjaga keseimbangan ekosistem bumi. Tahun 2025 ini, tema global yang diusung adalah “Mountains Matter for Water, Life, and Heritage”, menyoroti keterkaitan antara gunung, sumber air, keanekaragaman hayati, serta kebudayaan masyarakat yang hidup di sekitarnya.
                    <br>
                    Di Indonesia, perhatian terhadap kondisi ekosistem pegunungan semakin meningkat seiring munculnya berbagai dampak nyata perubahan iklim. Badan Meteorologi, Klimatologi, dan Geofisika (BMKG) mencatat, dalam lima tahun terakhir terjadi peningkatan suhu rata-rata di kawasan pegunungan sebesar 0,6°C yang mempercepat proses pencairan salju abadi di Puncak Jaya, Papua. Pencairan ini tidak hanya menandai hilangnya keajaiban alam tropis satu-satunya di dunia, tetapi juga mengancam pasokan air bagi ribuan masyarakat di lembah-lembah sekitarnya.
                    <br>
                    Selain itu, data dari Badan Geologi menunjukkan peningkatan kejadian tanah longsor di wilayah pegunungan Jawa Barat, Sumatera Barat, dan Sulawesi Selatan selama tahun 2024–2025. Hal ini disebabkan oleh kombinasi antara deforestasi, konversi lahan, serta curah hujan ekstrem yang terus meningkat. Di Kabupaten Garut dan Luwu, longsor besar merusak ratusan rumah dan menelan korban jiwa, menunjukkan bagaimana kerusakan ekosistem di lereng gunung dapat berdampak langsung pada kehidupan masyarakat bawahnya.
                    <br>
                    Tak hanya dari sisi lingkungan, perubahan kondisi pegunungan juga mengancam keanekaragaman hayati. Penelitian dari Badan Riset dan Inovasi Nasional (BRIN) pada awal 2025 menemukan bahwa beberapa spesies tumbuhan endemik seperti Edelweiss Jawa dan Nepenthes gymnamphora menunjukkan penurunan populasi signifikan akibat pergeseran suhu dan aktivitas manusia. Sementara itu, satwa seperti owa Jawa, anoa, dan burung cendrawasih menghadapi ancaman kehilangan habitat alami.
                    <br>
                    Namun, Hari Gunung Internasional tidak hanya menjadi ajang peringatan, tetapi juga panggilan untuk bertindak. Di berbagai daerah, gerakan masyarakat dan komunitas pecinta alam mulai menggalakkan program restorasi hutan gunung dan edukasi wisata berkelanjutan. Di Lombok, relawan Green Mountain Project menanam ribuan pohon di kawasan Rinjani, sementara di Jawa Tengah, komunitas Sahabat Merapi membangun sistem monitoring erupsi berbasis digital yang memadukan kearifan lokal dengan teknologi modern.
                    <br>
                    Hari Gunung Internasional 2025 menjadi pengingat bahwa pegunungan bukan sekadar destinasi wisata atau simbol keindahan alam, melainkan sumber kehidupan bagi jutaan makhluk hidup. Dari gunung mengalir air yang menyuburkan sawah, udara segar yang menjaga paru-paru bumi, hingga nilai spiritual yang mengikat manusia dengan alam. Dengan menjaga gunung, manusia sejatinya sedang menjaga masa depan peradabannya sendiri.
