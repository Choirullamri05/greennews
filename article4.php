                    <h2>Selamat Hari Bumi</h2>
                    Setiap tanggal 22 April, dunia memperingati Hari Bumi (Earth Day) sebagai momen refleksi dan aksi untuk melindungi planet yang menjadi satu-satunya rumah bagi seluruh makhluk hidup. Sejak pertama kali diperingati pada tahun 1970 di Amerika Serikat oleh aktivis lingkungan Gaylord Nelson, Hari Bumi telah berkembang menjadi gerakan global yang diikuti lebih dari 190 negara, termasuk Indonesia. Pada tahun 2025, tema yang diusung oleh Earth Day Network adalah “Planet vs. Plastics”, yang menyerukan aksi nyata untuk mengurangi ketergantungan terhadap plastik sekali pakai dan melindungi ekosistem dari ancaman pencemaran plastik yang semakin parah.
                    <br>
                    Menurut laporan Program Lingkungan PBB (UNEP), lebih dari 400 juta ton plastik diproduksi setiap tahun di seluruh dunia, dan sekitar 19–23 juta ton di antaranya berakhir di lautan, sungai, dan danau. Di Indonesia sendiri, data dari Kementerian Lingkungan Hidup dan Kehutanan (KLHK) menunjukkan bahwa negara ini menjadi salah satu penyumbang terbesar limbah plastik laut di Asia Tenggara. Limbah ini tidak hanya mengancam biota laut seperti penyu, burung, dan ikan, tetapi juga masuk ke rantai makanan manusia dalam bentuk mikroplastik.
                    <br>
                    Dampak perubahan iklim juga menjadi fokus utama dalam peringatan Hari Bumi 2025. Berdasarkan riset dari Badan Riset dan Inovasi Nasional (BRIN) dan BMKG, suhu rata-rata di Indonesia meningkat sekitar 0,8°C dalam 30 tahun terakhir, yang berpotensi memicu cuaca ekstrem, kebakaran hutan, serta penurunan kualitas tanah dan air. Wilayah Kalimantan, Sumatera, dan Nusa Tenggara menjadi kawasan yang paling rentan terhadap kekeringan, sementara pesisir utara Jawa menghadapi ancaman abrasi dan naiknya permukaan laut yang terus meningkat setiap tahunnya.
                    <br>
                    Namun, Hari Bumi bukan hanya tentang kesadaran akan krisis, tetapi juga tentang harapan dan tindakan nyata. Di berbagai daerah di Indonesia, muncul gerakan masyarakat yang berfokus pada konservasi, penghijauan, dan inovasi ramah lingkungan. Komunitas Bumi Lestari Bandung, misalnya, telah mengembangkan eco-brick project dengan mengubah sampah plastik menjadi bahan bangunan alternatif. Di Bali, gerakan Bye Bye Plastic Bags yang digagas oleh anak muda terus menginspirasi banyak sekolah untuk menghapus penggunaan plastik sekali pakai. Sementara itu, program Penanaman Serentak Satu Juta Pohon oleh KLHK bekerja sama dengan perguruan tinggi berhasil menghijaukan kembali ribuan hektare lahan kritis di berbagai provinsi.
                    <br>
                    Selain aksi lapangan, inovasi teknologi juga memainkan peran penting dalam upaya penyelamatan bumi. Penggunaan energi terbarukan seperti pembangkit listrik tenaga surya (PLTS) dan angin terus dikembangkan untuk mengurangi ketergantungan pada bahan bakar fosil. Pemerintah Indonesia menargetkan energi baru terbarukan (EBT) mencapai 23% dari total bauran energi nasional pada tahun 2025, sejalan dengan komitmen global dalam Paris Agreement untuk menekan laju pemanasan global di bawah 1,5°C.
                    <br>
                    Momentum Hari Bumi 2025 menjadi pengingat bahwa menjaga bumi bukan hanya tugas aktivis atau lembaga lingkungan, tetapi tanggung jawab bersama seluruh umat manusia. Setiap langkah kecil — mulai dari mengurangi penggunaan plastik, menanam pohon, menghemat energi, hingga memilah sampah — adalah kontribusi besar bagi masa depan planet ini.
