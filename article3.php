                    <h2>Selamat Hari Hewan Sedunial</h2>
                    Setiap tanggal 4 Oktober, dunia memperingati Hari Hewan Sedunia (World Animal Day) sebagai bentuk penghormatan terhadap seluruh makhluk hidup di bumi dan upaya meningkatkan kesadaran global tentang pentingnya kesejahteraan hewan. Peringatan ini pertama kali diinisiasi pada tahun 1931 di Florence, Italia, dan bertepatan dengan hari peringatan Santo Fransiskus dari Asisi, pelindung hewan dan lingkungan. Tahun 2025 ini, tema yang diangkat adalah “Protecting All Species: Our Shared Responsibility”, yang menyoroti tanggung jawab manusia dalam menjaga keberlangsungan hidup seluruh spesies di tengah krisis ekologis dan perubahan iklim yang semakin mengkhawatirkan.
                    <br>
                    Di Indonesia, isu kesejahteraan hewan menjadi semakin penting seiring meningkatnya kasus eksploitasi, perburuan liar, serta perdagangan satwa dilindungi. Berdasarkan data dari Kementerian Lingkungan Hidup dan Kehutanan (KLHK), sepanjang tahun 2024 saja, tercatat lebih dari 1.200 kasus perdagangan ilegal satwa berhasil diungkap di berbagai daerah, melibatkan ribuan hewan seperti burung nuri, orangutan, trenggiling, dan kura-kura air tawar langka. Praktik tersebut tidak hanya mengancam kelestarian spesies, tetapi juga memperburuk rantai ekosistem yang bergantung satu sama lain.
                    <br>
                    Selain itu, Badan Riset dan Inovasi Nasional (BRIN) mencatat adanya penurunan populasi signifikan pada beberapa satwa endemik. Populasi badak Jawa (Rhinoceros sondaicus) kini diperkirakan hanya tersisa sekitar 70 ekor di Taman Nasional Ujung Kulon. Sementara itu, elang Jawa dan anoa Sulawesi juga berada dalam status “Terancam Punah” akibat penyempitan habitat yang terus berlanjut akibat deforestasi, kebakaran hutan, dan perambahan lahan. Kondisi ini menggambarkan bahwa hilangnya satu spesies bukan sekadar kehilangan hewan, tetapi juga keruntuhan keseimbangan ekosistem secara menyeluruh.
                    <br>
                    Kesejahteraan hewan domestik pun tak kalah penting. Di berbagai kota besar seperti Jakarta, Surabaya, dan Medan, masih banyak ditemukan kasus penelantaran hewan peliharaan. Organisasi pecinta hewan seperti Jakarta Animal Aid Network (JAAN) dan Animal Defenders Indonesia melaporkan peningkatan jumlah adopsi hewan selama pandemi, namun diikuti pula oleh meningkatnya angka penelantaran setelah situasi kembali normal. Kondisi ini menunjukkan bahwa masih banyak masyarakat yang belum memahami tanggung jawab moral dan sosial dalam memelihara hewan dengan layak.
                    <br>
                    Meski begitu, peringatan Hari Hewan Sedunia juga membawa semangat perubahan. Di berbagai daerah, komunitas pecinta hewan dan lembaga konservasi terus bergerak aktif dalam kegiatan edukasi, vaksinasi gratis, hingga kampanye anti-kekerasan terhadap hewan. Di Yogyakarta misalnya, gerakan Love Paw Indonesia menggelar acara Run for Animals untuk menggalang dana bagi penampungan hewan jalanan. Sementara itu, di Kalimantan Timur, proyek Orangutan Release Program yang dikelola Yayasan BOS berhasil melepaskan puluhan individu orangutan hasil rehabilitasi ke habitat aslinya sepanjang tahun 2025.
                    <br>
                    Dari sisi kebijakan, pemerintah Indonesia mulai memperkuat regulasi terkait perlindungan satwa melalui penerapan Undang-Undang Nomor 5 Tahun 1990 yang kini tengah direvisi untuk menyesuaikan dengan perkembangan zaman. Selain itu, dukungan terhadap One Health Approach — pendekatan terpadu antara kesehatan manusia, hewan, dan lingkungan — mulai diterapkan dalam kebijakan kesehatan nasional, khususnya dalam pencegahan zoonosis seperti rabies dan flu burung.
                    <br>
                    Peringatan Hari Hewan Sedunia 2025 menjadi pengingat bahwa seluruh makhluk hidup memiliki hak untuk hidup aman, sehat, dan bebas dari penderitaan. Hewan bukan sekadar bagian dari alam, tetapi juga bagian dari keberlanjutan hidup manusia. Dengan menjaga mereka, manusia sejatinya sedang menjaga keseimbangan bumi yang menjadi rumah bersama. Di tengah krisis lingkungan global, kasih sayang dan kepedulian terhadap hewan bukan lagi pilihan — melainkan sebuah tanggung jawab moral dan kemanusiaan.
