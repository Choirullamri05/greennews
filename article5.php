                    <h2>Selamat Hari Bumi</h2>
                    Setiap tanggal 16 Oktober, dunia memperingati Hari Pangan Sedunia (World Food Day) sebagai wujud penghormatan atas berdirinya Organisasi Pangan dan Pertanian Perserikatan Bangsa-Bangsa (FAO) pada tahun 1945. Peringatan ini menjadi momentum global untuk meningkatkan kesadaran akan pentingnya pangan yang cukup, bergizi, dan berkelanjutan bagi semua orang. Pada tahun 2025, FAO mengusung tema “Water is Life, Water is Food – Leave No One Behind”, yang menyoroti pentingnya air sebagai sumber utama dalam sistem pangan dan menegaskan bahwa pengelolaan air yang berkelanjutan menjadi kunci bagi masa depan ketahanan pangan dunia.
                    <br>
                    Menurut laporan FAO, lebih dari 2,4 miliar orang di dunia menghadapi kesulitan dalam mengakses makanan bergizi setiap hari. Sementara itu, sekitar 600 juta penduduk dunia masih hidup dalam kondisi rawan pangan. Di Indonesia, Badan Pangan Nasional (Bapanas) mencatat bahwa tantangan utama dalam sektor pangan adalah ketergantungan terhadap impor bahan pokok seperti gandum dan kedelai, serta kerentanan pasokan akibat cuaca ekstrem dan perubahan iklim. Fenomena El Niño 2024–2025 berdampak besar pada sektor pertanian nasional, menyebabkan penurunan produksi padi hingga 7% di beberapa provinsi seperti Jawa Tengah, Nusa Tenggara Timur, dan Sulawesi Selatan.
                    <br>
                    Krisis air juga menjadi perhatian serius. Berdasarkan data Kementerian Pertanian (Kementan), lebih dari 30% lahan irigasi di Indonesia mengalami kekeringan musiman yang berdampak pada produktivitas pertanian. Di sisi lain, pencemaran air akibat limbah pertanian dan industri turut memperburuk kualitas sumber air. Kondisi ini menunjukkan bahwa ketahanan pangan dan pengelolaan sumber daya air memiliki keterkaitan erat — tanpa air yang cukup dan bersih, produksi pangan yang berkelanjutan tidak mungkin terwujud.
                    <br>
                    Meski begitu, berbagai langkah inovatif telah dilakukan untuk memperkuat sistem pangan nasional. Program Food Estate yang dijalankan pemerintah di Kalimantan Tengah dan Sumatera Utara terus dikembangkan sebagai lumbung pangan modern berbasis teknologi. Selain itu, penelitian dari Badan Riset dan Inovasi Nasional (BRIN) juga menyoroti pentingnya penerapan pertanian presisi (precision agriculture) yang menggunakan sensor tanah, data satelit, dan kecerdasan buatan (AI) untuk mengoptimalkan penggunaan air dan pupuk, serta meningkatkan produktivitas tanaman tanpa merusak lingkungan.
                    <br>
                    Peran masyarakat juga semakin penting dalam menciptakan ketahanan pangan lokal. Di berbagai daerah, gerakan urban farming dan pertanian organik mulai berkembang pesat. Komunitas Kota Hijau Jakarta misalnya, berhasil mengubah lahan sempit di perkotaan menjadi kebun produktif dengan sistem hidroponik dan aquaponik. Di Yogyakarta, Gerakan Pangan Lestari menggandeng petani muda untuk memanfaatkan limbah rumah tangga sebagai pupuk kompos alami. Inovasi-inovasi seperti ini tidak hanya membantu ketahanan pangan keluarga, tetapi juga membangun kesadaran generasi muda terhadap pentingnya kemandirian pangan.
